package com.roiim.restapi.pojo;

public class Address {

    public String id;

    public String nickName;

    public String street;

    public String street2;

    public String city;

    public String state;

    public String country;

    public String zip;

    public String phone;

    public String status;

    public Boolean defaultShippingAddressIndicator;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getStreet2() {
		return street2;
	}

	public void setStreet2(String street2) {
		this.street2 = street2;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getZip() {
		return zip;
	}

	public void setZip(String zip) {
		this.zip = zip;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public Boolean getDefaultShippingAddressIndicator() {
		return defaultShippingAddressIndicator;
	}

	public void setDefaultShippingAddressIndicator(Boolean defaultShippingAddressIndicator) {
		this.defaultShippingAddressIndicator = defaultShippingAddressIndicator;
	}
    
    
}
